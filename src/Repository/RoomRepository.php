    public function getLastStory(Room $room): ?int
    {
        $qb = $this->createQueryBuilder('r')
            ->select('MAX(s.id)')
            ->from('App\Entity\Story', 's')
            ->where('s.room = :room')
            ->setParameter('room', $room)
            ->getQuery()
            ->getSingleScalarResult();

        return $qb;
    }
